(defn- resolve-events
  "Resolves :grain/read-models map to a union of their :events sets.
   read-models is a map of {rm-name version, ...}.
   The version is declarative — multiple versions can co-exist."
  [read-models]
  (let [registry @rmp/read-model-registry*]
    (into #{}
          (mapcat (fn [[rm-name _version]]
                    (:events (get registry rm-name))))
          read-models)))

(defn- resolve-event-tags
  "Resolves a {:tag-key :query-key-or-path} map against query-context into
   a predicate (fn [event] -> boolean) suitable for a channel transducer.
   Bare keyword values resolve from [:query k]; vectors are full get-in paths."
  [event-tags query-context]
  (let [resolved (into #{}
                       (map (fn [[tag-key path]]
                              [tag-key (if (vector? path)
                                         (get-in query-context path)
                                         (get-in query-context [:query path]))]))
                       event-tags)]
    (fn [event]
      (set/subset? resolved (:event/tags event)))))

(defn- subscribe-to-events
  "Creates a sliding-buffer channel and subscribes it to each event type via pubsub.
   When event-filter-fn is provided, applies it as a transducer on the channel.
   Returns the subscription channel."
  [event-pubsub event-types event-filter-fn]
  (let [buf (async/sliding-buffer 64)
        sub-chan (if event-filter-fn
                  (async/chan buf (filter event-filter-fn))
                  (async/chan buf))]
    (doseq [event-type event-types]
      (pubsub/sub event-pubsub {:topic event-type :sub-chan sub-chan}))
    sub-chan))

(defn- drain-channel
  "Non-blocking drain of all buffered messages on ch. Returns count drained."
  [ch]
  (loop [n 0]
    (if-let [_ (async/poll! ch)]
      (recur (inc n))
      n)))

(defn- stream-view-loop-events
  "Event-driven loop for stream-view. Subscribes to event types, re-renders on events.
   Sub-chan is created BEFORE initial render to avoid missing events.
   Query context is stored in an atom so @post can update signals mid-stream."
  [event-ch sse-ctx context query-name event-pubsub event-types debounce-ms additional-context event-tags]
  (let [request (:request sse-ctx)
        raw-query (extract-query-from-request request query-name)
        nonce (or (:dsNonce raw-query) (get raw-query "dsNonce"))
        decoded-query (decode-json-query (dissoc raw-query :dsNonce "dsNonce"))
        initial-context (merge context additional-context {:query decoded-query})
        context-atom (atom initial-context)
        signal-ch (async/chan (async/sliding-buffer 1))
        user-id (get-in additional-context [:auth-claims :user-id])
        session-key [user-id query-name nonce]
        _ (swap! active-stream-contexts assoc session-key
                 {:context-atom context-atom :signal-ch signal-ch :event-ch event-ch})
        event-filter-fn (when event-tags
                          (resolve-event-tags event-tags @context-atom))
        sub-chan (subscribe-to-events event-pubsub event-types event-filter-fn)]
    (try
      ;; Initial render
      (let [initial (poll-and-render @context-atom nil)]
        (when initial
          (async/>!! event-ch (:event initial)))
        (when (:stop? initial)
          (throw (ex-info "stop" {:stop true})))
        ;; Event loop — listens to domain events AND signal updates
        (loop [prev-result (:result initial)]
          (let [[val _port] (async/alts!! [sub-chan signal-ch (async/timeout 30000)])]
            (cond
              ;; Channel closed (pubsub shutting down or signal-ch closed)
              (and (nil? val) (or (async-protocols/closed? sub-chan)
                                  (async-protocols/closed? signal-ch)))
              nil

              ;; Timeout — liveness check
              (nil? val)
              (if (async-protocols/closed? event-ch)
                nil
                (recur prev-result))

              ;; Event or signal update received. Debounce then drain BOTH channels
              ;; before re-rendering. Draining both is a correctness requirement, not
              ;; just a performance optimization: if a domain event and a signal update
              ;; arrive near-simultaneously, we must apply the signal change before
              ;; re-rendering — otherwise the query runs with stale signals and the
              ;; user sees a flash of old state before the signal update triggers
              ;; another render cycle.
              :else
              (do
                (when (pos? debounce-ms) (Thread/sleep (long debounce-ms)))
                (drain-channel sub-chan)
                (drain-channel signal-ch)
                (let [poll-result (poll-and-render @context-atom prev-result)]
                  (cond
                    (:stop? poll-result)
                    (async/>!! event-ch (:event poll-result))

                    poll-result
                    (let [sent? (async/alt!!
                                  [[event-ch (:event poll-result)]] true
                                  :default false)]
                      (if sent?
                        (recur (:result poll-result))
                        nil))

                    :else
                    (recur prev-result))))))))
      (catch Exception e
        (when-not (= "stop" (.getMessage e))
          (u/log ::stream-view-events-error :error e)))
      (finally
        (swap! active-stream-contexts dissoc session-key)
        (async/close! signal-ch)
        (async/close! sub-chan)
        (async/close! event-ch)))))

(defn- stream-view-enter-events
  "Enter fn for stream-view interceptor (event-driven mode)."
  [context query-name event-pubsub event-types debounce-ms heartbeat-delay event-tags pedestal-context]
  (let [additional-context (:grain/additional-context pedestal-context)]
    (sse/start-stream
      (fn [event-ch sse-ctx]
        (stream-view-loop-events event-ch sse-ctx context query-name
                                 event-pubsub event-types debounce-ms additional-context event-tags))
      pedestal-context
      heartbeat-delay)))

(defn- update-or-start-stream-events
  "If an active event-driven SSE exists for this user+query+nonce, update its
   context atom with new signals and trigger a re-render via signal-ch, then
   return an empty SSE that closes immediately. Otherwise start a new SSE.
   Works for both GET and POST — enables signal updates without reconnecting.
   For GET requests, session reuse requires a nonce (dsNonce) to avoid
   accidental collisions; POST always attempts reuse for backward compatibility."
  [context query-name event-pubsub event-types debounce-ms heartbeat-delay event-tags pedestal-context]
  (let [additional-context (:grain/additional-context pedestal-context)
        request (:request pedestal-context)
        raw-query (extract-query-from-request request query-name)
        nonce (or (:dsNonce raw-query) (get raw-query "dsNonce"))
        is-post? (= :post (:request-method request))
        user-id (get-in additional-context [:auth-claims :user-id])
        session-key [user-id query-name nonce]
        ;; For GET, only attempt reuse when a nonce is present — nonce is the
        ;; explicit opt-in for session reuse (generated by shim-page per page load).
        ;; Without nonce, GET always starts fresh to avoid accidental collisions.
        ;; POST always attempts reuse for backward compatibility.
        existing (when (or is-post? nonce)
                   (get @active-stream-contexts session-key))
        ;; Only use existing if both signal-ch and event-ch are still open.
        ;; event-ch closes when the browser disconnects (page navigation);
        ;; signal-ch closes when the event loop's finally block runs.
        existing (when (and existing
                            (not (async-protocols/closed? (:signal-ch existing)))
                            (not (async-protocols/closed? (:event-ch existing))))
                   existing)]
    (if existing
      ;; Update existing SSE's context with new signals (from :query-params, already parsed by interceptor)
      (let [new-decoded (decode-json-query (dissoc raw-query :dsNonce "dsNonce"))
            {:keys [context-atom signal-ch]} existing]
        (swap! context-atom assoc :query new-decoded)
        (async/put! signal-ch :signal-update)
        ;; Return empty SSE — close immediately. The real re-render happens
        ;; on the existing SSE via signal-ch.
        (sse/start-stream (fn [ch _] (async/close! ch)) pedestal-context heartbeat-delay))
      ;; No existing SSE (or stale) — clean up stale entry and start fresh.
      ;; Close old signal-ch so the orphaned event loop exits cleanly.
      (do
        (when (or is-post? nonce)
          (when-let [stale (get @active-stream-contexts session-key)]
            (async/close! (:signal-ch stale))))
        (stream-view-enter-events context query-name event-pubsub event-types
                                  debounce-ms heartbeat-delay event-tags pedestal-context)))))

(defn stream-view
  "Interceptor factory. Streams :datastar/hiccup via SSE.

   Modes:
   - Event-driven: when :event-types is non-empty AND :event-pubsub is in context,
     re-renders only when relevant events fire. Both GET and POST requests check
     for an existing SSE (by user+query+nonce) and reuse it for signal updates
     without reconnecting. If no existing SSE is found, a fresh one is started.
   - Polling: when :fps is positive, polls at the given FPS.
   - One-shot: when :fps is 0 or nil, renders once and closes."
  [context query-name opts]
  (let [{:keys [fps heartbeat-delay event-types debounce-ms event-tags]
         :or {fps 30 heartbeat-delay 10 debounce-ms 50}} opts
        event-pubsub (:event-pubsub context)]
    (if (and (seq event-types) event-pubsub)
      ;; Event-driven mode — both GET and POST check for existing SSE first
      {:name (keyword "ai.obney.grain.datastar.core" (str "stream-" (name query-name)))
       :enter (fn [pedestal-context]
                (update-or-start-stream-events context query-name event-pubsub event-types
                                                debounce-ms heartbeat-delay event-tags pedestal-context))}
      ;; Polling / one-shot mode (unchanged)
      (let [interval-ms (when (and fps (pos? fps))
                          (long (/ 1000.0 fps)))]
        {:name (keyword "ai.obney.grain.datastar.core" (str "stream-" (name query-name)))
         :enter #(stream-view-enter context query-name interval-ms heartbeat-delay %)}))))

