(defn- query-name->route-name
  "Converts :user/sign-in-page to ::user-sign-in-page"
  [query-name]
  (keyword "ai.obney.grain.datastar.core"
           (str (namespace query-name) "-" (name query-name))))

(defn- query->route-pair
  "Given a query-name and its registry entry, returns [shim-route stream-route].
   Returns nil if the entry has no :datastar/path.
   defaults is an optional map with :datastar/shim-opts and :datastar/auth-redirect."
  [context query-name entry defaults]
  (when-let [path (:datastar/path entry)]
    (let [stream-path (if (= path "/") "/__stream" (str path "/__stream"))
          title (or (:datastar/title entry) "Grain App")
          fps (:datastar/fps entry 30)
          explicit-interceptors (or (:datastar/interceptors entry) [])
          ;; Auto-generate auth-redirect interceptor when configured in defaults
          ;; and the query's :authorized? predicate rejects empty context (non-public).
          auth-redirect-config (:datastar/auth-redirect defaults)
          authorized? (:authorized? entry)
          public? (and authorized? (try (authorized? {}) (catch Exception _ false)))
          auth-interceptor (when (and auth-redirect-config authorized? (not public?))
                             (auth-redirect-interceptor
                              (assoc auth-redirect-config :authorized? authorized?)))
          ;; Auto-generate gate interceptor when :datastar/gate is declared.
          ;; Gate redirect falls back to :unauthorized from auth-redirect defaults.
          gate-config (:datastar/gate entry)
          gate-int (when gate-config
                     (let [redirect (or (:redirect gate-config)
                                        (get-in defaults [:datastar/auth-redirect :unauthorized]))]
                       (gate-interceptor context (assoc gate-config :redirect redirect))))
          interceptors (cond-> []
                         auth-interceptor (conj auth-interceptor)
                         gate-int (conj gate-int)
                         true (into explicit-interceptors))
          shim-opts (merge {:title title :stream-path stream-path}
                           (:datastar/shim-opts defaults)
                           (:datastar/shim-opts entry))
          route-base (query-name->route-name query-name)
          read-models (:grain/read-models entry)
          event-types (when (seq read-models) (resolve-events read-models))
          debounce-ms (:datastar/debounce-ms entry 50)
          event-tags (:datastar/event-tags entry)
          stream-opts (cond-> {:fps fps}
                        (seq event-types) (assoc :event-types event-types
                                                 :debounce-ms debounce-ms)
                        event-tags (assoc :event-tags event-tags))]
      (let [with-signals (fn [extra]
                          (into [parse-datastar-signals] (concat interceptors extra)))]
        [;; Shim page route (GET)
         [path :get (with-signals [(shim-page shim-opts)])
          :route-name (keyword (namespace route-base)
                               (str (name route-base) "-page"))]
         ;; Stream route (GET — initial page load via @get)
         [stream-path :get (with-signals [(stream-view context query-name stream-opts)])
          :route-name (keyword (namespace route-base)
                               (str (name route-base) "-stream"))]
         ;; Stream route (POST — @post sends signals in body)
         [stream-path :post (with-signals [(stream-view context query-name stream-opts)])
          :route-name (keyword (namespace route-base)
                               (str (name route-base) "-stream-post"))]]))))

(defn routes
  "Scans the query registry for entries with :datastar/path and generates
   Pedestal route pairs (shim-page + stream-view) for each.

   Optional overrides map: {query-name {:datastar/fps 2 :datastar/interceptors [...]}}
   Optional defaults map: {:datastar/shim-opts {:head fn :html-attrs {} :datastar-url str}}
     Defaults are applied to all routes; per-query shim-opts override them."
  ([context] (routes context {} {}))
  ([context overrides] (routes context overrides {}))
  ([context overrides defaults]
   (let [registry (or (:query-registry context) @qp/query-registry*)]
     (into #{}
           (comp
            (filter (fn [[_ entry]] (:datastar/path entry)))
            (mapcat (fn [[qname entry]]
                      (let [merged (merge entry (get overrides qname))]
                        (query->route-pair context qname merged defaults))))
            (remove nil?))
           registry))))
