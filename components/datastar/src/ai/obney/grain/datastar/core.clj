(defn- decode-json-query
  "Coerces query params from string values into proper EDN types
   using the query's registered Malli schema + json-transformer."
  [raw-query]
  (let [query-name (:query/name raw-query)
        query-schema (try (when query-name (mc/schema query-name))
                          (catch Exception e
                            (u/log ::missing-query-schema
                                   :query-name query-name
                                   :error (.getMessage e))
                            nil))
        decoded (if query-schema
                  (mc/decode query-schema raw-query (mt/json-transformer))
                  raw-query)]
    (assoc decoded
           :query/name query-name
           :query/id (random-uuid)
           :query/timestamp (time/now))))

(defn- parse-signals [request]
  (when-let [body (:body request)]
    (let [body-str (if (string? body) body (slurp body))]
      (when (seq body-str)
        (json/read-str body-str :key-fn keyword)))))

(defn- stringify-signal-values
  "Coerce signal values to strings to match GET query-param semantics.
   Skips maps and collections (e.g. fieldErrors) — only primitives are stringified."
  [signals]
  (reduce-kv (fn [m k v]
               (assoc m k (cond
                            (string? v) v
                            (nil? v) ""
                            (or (map? v) (sequential? v)) v
                            :else (str v))))
             {} signals))

(def parse-datastar-signals
  "Interceptor that extracts Datastar signals from the `datastar` query param (GET)
   or JSON body (POST) and merges them into the request's :query-params map.
   Auto-included by `query->route-pair` on all generated routes.

   POST body formats:
   - Wrapped:  {\"datastar\": {\"key\": \"val\"}}  (older Datastar convention)
   - Flat:     {\"key\": \"val\"}                   (Datastar RC.7+)

   Both GET and POST paths merge native JSON types (integers stay integers)."
  {:name ::parse-datastar-signals
   :enter
   (fn [ctx]
     (let [ds-param (or (get-in ctx [:request :query-params :datastar])
                        (get-in ctx [:request :query-params "datastar"]))
           ds-body (when (and (nil? ds-param)
                              (= :post (get-in ctx [:request :request-method])))
                     (try
                       (when-let [body (get-in ctx [:request :body])]
                         (let [body-str (if (string? body) body (slurp body))]
                           (when (seq body-str)
                             (let [parsed (json/read-str body-str :key-fn keyword)]
                               ;; Unwrap {:datastar {...}} if present, otherwise use flat signals
                               (or (:datastar parsed) parsed)))))
                       (catch Exception _ nil)))]
       (cond
         ds-param
         (try
           (let [signals (json/read-str ds-param :key-fn keyword)]
             (update-in ctx [:request :query-params] merge signals))
           (catch Exception _ ctx))

         ds-body
         (update-in ctx [:request :query-params] merge ds-body)

         :else ctx)))})

(defn- decode-json-command
  "Coerces a raw JSON-parsed command map into proper EDN types using
   the command's registered Malli schema + json-transformer."
  [raw-command]
  (let [command-name (let [n (:command/name raw-command)]
                       (cond
                         (keyword? n) n
                         (string? n)  (keyword (cond-> n (string/starts-with? n ":") (subs 1)))
                         :else n))
        raw-command (assoc raw-command :command/name command-name)
        command-schema (try (when command-name (mc/schema command-name))
                            (catch Exception _ nil))
        decoded (if command-schema
                  (mc/decode command-schema raw-command
                            (mt/transformer (mt/json-transformer)
                                            (mt/strip-extra-keys-transformer)))
                  raw-command)]
    (assoc decoded
           :command/name command-name
           :command/id (random-uuid)
           :command/timestamp (time/now))))

;; -------------------------------- ;;
;; Active Stream Context Registry   ;;
;; -------------------------------- ;;

;; Registry of active event-driven SSE connections.
;; Key:   [user-id query-name nonce] — nonce is a per-page-load UUID so
;;        multiple tabs viewing the same query get independent streams.
;; Value: {:context-atom atom, :signal-ch channel, :event-ch channel}
;;
;; Lifecycle: entries are added in `stream-view-loop-events` when a GET
;; opens a new SSE, and removed in its `finally` block when the stream
;; closes (browser navigates away or server shuts down).
;;
;; Purpose: `update-or-start-stream-events` looks up an existing entry
;; on @post to push new signals into the persistent SSE instead of
;; opening a duplicate connection.
(def ^:private active-stream-contexts (atom {}))

