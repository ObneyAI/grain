;; Auth-Redirect Interceptor     ;;
;; ----------------------------- ;;

(defn auth-redirect-interceptor
  "Interceptor factory. Checks authorization via the query's :authorized? predicate
   and redirects with 302 when the user is unauthenticated or unauthorized.

   opts:
     :authorized?      — predicate fn, receives {:auth-claims claims}
     :unauthenticated  — redirect path when no claims present (e.g. \"/auth/sign-in\")
     :unauthorized     — redirect path when claims present but predicate fails (e.g. \"/dashboard\")"
  [{:keys [authorized? unauthenticated unauthorized]}]
  {:name ::auth-redirect
   :enter
   (fn [ctx]
     (let [claims (get-in ctx [:grain/additional-context :auth-claims])]
       (cond
         (authorized? {:auth-claims claims}) ctx
         (not claims) (assoc ctx :response {:status 302 :headers {"Location" unauthenticated}})
         :else (assoc ctx :response {:status 302 :headers {"Location" unauthorized}}))))})

;; ---------------------- ;;
;; Gate Interceptor        ;;
;; ---------------------- ;;

(defn gate-interceptor
  "Interceptor factory. Calls check-fn with the grain context merged with
   per-request auth claims. Redirects with 302 on falsy result.

   context  — grain system context (event store, cache, etc.)
   gate-opts:
     :check    — (fn [context] boolean), receives grain context + auth claims
     :redirect — path to redirect to on failure"
  [context {:keys [check redirect]}]
  {:name ::gate
   :enter
   (fn [ctx]
     (let [additional (get ctx :grain/additional-context)
           gate-ctx (merge context additional)
           result (check gate-ctx)]
       (if result
         ctx
         (assoc ctx :response
                {:status 302
                 :headers {"Location" redirect}}))))})

